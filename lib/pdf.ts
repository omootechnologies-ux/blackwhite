type BasicPdfLine = {
  text: string
  size: number
  bold: boolean
  spacer?: boolean
}

function pdfLine(text: string, size = 10, bold = false): BasicPdfLine {
  return { text, size, bold }
}

function pdfSpacer(): BasicPdfLine {
  return { text: '', size: 8, bold: false, spacer: true }
}

function createBasicTextPDF(lines: BasicPdfLine[]): Buffer {
  const pageWidth = 595.28
  const pageHeight = 841.89
  const marginX = 42
  const marginTop = 48
  const marginBottom = 42
  const usableWidth = pageWidth - marginX * 2
  const pages: BasicPdfLine[][] = [[]]
  let y = pageHeight - marginTop

  for (const line of lines) {
    const wrappedLines = line.spacer ? [''] : wrapText(line.text, Math.max(24, Math.floor(usableWidth / (line.size * 0.52))))

    for (const text of wrappedLines) {
      const height = line.spacer ? 10 : line.size + 5
      if (y - height < marginBottom) {
        pages.push([])
        y = pageHeight - marginTop
      }

      pages[pages.length - 1].push({ ...line, text })
      y -= height
    }
  }

  const objects: string[] = []
  const addObject = (body: string) => {
    objects.push(body)
    return objects.length
  }

  const catalogId = addObject('<< /Type /Catalog /Pages 2 0 R >>')
  const pagesId = addObject('')
  const regularFontId = addObject('<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>')
  const boldFontId = addObject('<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>')
  const pageIds: number[] = []

  pages.forEach((pageLines) => {
    const content = renderPdfPageContent(pageLines, pageHeight, marginX, marginTop)
    const contentId = addObject(`<< /Length ${Buffer.byteLength(content)} >>\nstream\n${content}\nendstream`)
    const pageId = addObject(
      `<< /Type /Page /Parent ${pagesId} 0 R /MediaBox [0 0 ${pageWidth} ${pageHeight}] ` +
        `/Resources << /Font << /F1 ${regularFontId} 0 R /F2 ${boldFontId} 0 R >> >> ` +
        `/Contents ${contentId} 0 R >>`
    )
    pageIds.push(pageId)
  })

  objects[pagesId - 1] = `<< /Type /Pages /Kids [${pageIds.map((id) => `${id} 0 R`).join(' ')}] /Count ${pageIds.length} >>`

  const chunks = ['%PDF-1.4\n']
  const offsets: number[] = [0]

  objects.forEach((body, index) => {
    offsets.push(Buffer.byteLength(chunks.join('')))
    chunks.push(`${index + 1} 0 obj\n${body}\nendobj\n`)
  })

  const xrefOffset = Buffer.byteLength(chunks.join(''))
  chunks.push(`xref\n0 ${objects.length + 1}\n`)
  chunks.push('0000000000 65535 f \n')
  offsets.slice(1).forEach((offset) => {
    chunks.push(`${String(offset).padStart(10, '0')} 00000 n \n`)
  })
  chunks.push(`trailer\n<< /Size ${objects.length + 1} /Root ${catalogId} 0 R >>\nstartxref\n${xrefOffset}\n%%EOF\n`)

  return Buffer.from(chunks.join(''), 'utf8')
}

function renderPdfPageContent(lines: BasicPdfLine[], pageHeight: number, marginX: number, marginTop: number) {
  let y = pageHeight - marginTop
  const commands: string[] = ['BT']

  lines.forEach((line) => {
    if (line.spacer) {
      y -= 10
      return
    }

    commands.push(`/${line.bold ? 'F2' : 'F1'} ${line.size} Tf`)
    commands.push(`1 0 0 1 ${marginX} ${y.toFixed(2)} Tm`)
    commands.push(`(${escapePdfText(line.text)}) Tj`)
    y -= line.size + 5
  })

  commands.push('ET')
  return commands.join('\n')
}

function wrapText(text: string, maxChars: number) {
  const clean = cleanPdfText(text)
  if (clean.length <= maxChars) return [clean]

  const words = clean.split(/\s+/)
  const lines: string[] = []
  let current = ''

  words.forEach((word) => {
    if (!current) {
      current = word
      return
    }

    if (`${current} ${word}`.length <= maxChars) {
      current = `${current} ${word}`
    } else {
      lines.push(current)
      current = word
    }
  })

  if (current) lines.push(current)
  return lines
}

function cleanPdfText(text: string) {
  return String(text || '')
    .normalize('NFKD')
    .replace(/[^\x20-\x7E]/g, '')
}

function escapePdfText(text: string) {
  return cleanPdfText(text).replace(/\\/g, '\\\\').replace(/\(/g, '\\(').replace(/\)/g, '\\)')
}

function joinPresent(values: (string | null | undefined)[]) {
  return values.filter(Boolean).join(' | ')
}

async function renderLogoHTML(business: Business, className: string) {
  const fallback = `<div class="logo-placeholder">${escapeHtml((business.name || 'B').charAt(0))}</div>`

  if (!business.logo_url) return fallback

  const src = await logoUrlToDataUri(business.logo_url)
  if (!src) return fallback

  return `<img src="${src}" alt="${escapeHtml(business.name)}" class="${className}" />`
}

async function logoUrlToDataUri(url: string) {
  try {
    const response = await fetch(url, { cache: 'no-store' })
    if (!response.ok) return url

    const contentType = response.headers.get('content-type') || 'image/png'
    if (!contentType.startsWith('image/')) return url

    const arrayBuffer = await response.arrayBuffer()
    if (arrayBuffer.byteLength > 2_500_000) return url

    return `data:${contentType};base64,${Buffer.from(arrayBuffer).toString('base64')}`
  } catch {
    return url
  }
}

function escapeHtml(value: string | number | null | undefined) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;')
}

