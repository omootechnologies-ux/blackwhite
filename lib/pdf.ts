// Uses @sparticuz/chromium for serverless environments
// ============================================================

async function getBrowser() {
  // In production (Vercel/serverless), use stripped Chromium
  if (process.env.NODE_ENV === 'production') {
    const chromium = (await import('@sparticuz/chromium')).default
    const puppeteer = await import('puppeteer-core')
    return puppeteer.launch({
      args: chromium.args,
      defaultViewport: chromium.defaultViewport,
      executablePath: await chromium.executablePath(),
      headless: true,
    })
  }

  // Local dev: use a local Chrome/Chromium executable with puppeteer-core.
  const puppeteer = await import('puppeteer-core')
  const executablePath = findLocalChrome()

  return puppeteer.launch({
    headless: true,
    executablePath,
    args: ['--no-sandbox', '--disable-setuid-sandbox'],
  })
}

function findLocalChrome() {
  const candidates = [
    process.env.PUPPETEER_EXECUTABLE_PATH,
    process.platform === 'win32'
      ? 'C:\\Program Files\\Google\\Chrome\\Application\\chrome.exe'
      : null,
    process.platform === 'darwin'
      ? '/Applications/Google Chrome.app/Contents/MacOS/Google Chrome'
      : null,
    '/usr/bin/google-chrome',
    '/usr/bin/chromium-browser',
    '/usr/bin/chromium',
  ].filter(Boolean) as string[]

  const executablePath = candidates.find((path) => existsSync(path))

  if (!executablePath) {
    throw new Error(
      'Chrome executable not found. Install Google Chrome locally or set PUPPETEER_EXECUTABLE_PATH.'
    )
  }

  return executablePath
}

export async function htmlToPDF(html: string): Promise<Buffer> {
  const browser = await getBrowser()
  try {
    const page = await browser.newPage()
    await page.setContent(html, { waitUntil: 'networkidle0' })
    const pdf = await page.pdf({
      format: 'A4',
      printBackground: true,
      margin: { top: '15mm', right: '15mm', bottom: '15mm', left: '15mm' },
    })
    return Buffer.from(pdf)
  } finally {
    await browser.close()
  }
}

export function renderInvoiceFallbackPDF(invoice: Invoice, business: Business): Buffer {
  const lines = [
    pdfLine(business.name, 18, true),
    pdfLine(joinPresent([business.address, business.phone, business.email]), 9),
    pdfLine(joinPresent([business.tin ? `TIN: ${business.tin}` : '', business.vrn ? `VRN: ${business.vrn}` : '']), 9),
    pdfSpacer(),
    pdfLine(`INVOICE ${invoice.number}`, 16, true),
    pdfLine(`Client: ${invoice.client_name}`, 11, true),
    pdfLine(joinPresent([invoice.client_phone, invoice.client_email, invoice.client_tin ? `TIN: ${invoice.client_tin}` : '']), 9),
    pdfLine(`Invoice date: ${formatDate(invoice.created_at)}`, 9),
    pdfLine(`Due date: ${invoice.due_date ? formatDate(invoice.due_date) : 'As soon as possible'}`, 9),
    pdfSpacer(),
    pdfLine('Items', 12, true),
    ...invoice.items.flatMap((item) => [
      pdfLine(`${item.description}`, 10, true),
      pdfLine(`Qty ${item.qty} x ${formatTZS(item.unit_price)} = ${formatTZS(item.amount)}`, 9),
    ]),
    pdfSpacer(),
    pdfLine(`Subtotal: ${formatTZS(invoice.subtotal)}`, 10),
    pdfLine(`VAT (${invoice.vat_rate}%): ${formatTZS(invoice.vat_amount)}`, 10),
    pdfLine(`Grand total: ${formatTZS(invoice.total)}`, 13, true),
  ]

  if (invoice.payment_link) {
    lines.push(pdfSpacer(), pdfLine('Payment reference', 11, true), pdfLine(invoice.payment_link, 9))
  }

  if (invoice.notes) {
    lines.push(pdfSpacer(), pdfLine('Notes', 11, true), pdfLine(invoice.notes, 9))
  }

  lines.push(
    pdfSpacer(),
    pdfLine(`Generated by Blackwhite for ${business.name}`, 8)
  )

  return createBasicTextPDF(lines)
}

