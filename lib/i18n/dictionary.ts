export const LANGUAGE_COOKIE = 'blackwhite_lang'
export const DEFAULT_LOCALE = 'en'
export const SUPPORTED_LOCALES = ['en', 'sw'] as const

export type Locale = (typeof SUPPORTED_LOCALES)[number]
export type TranslationKey = keyof typeof dictionaries.en

type Variables = Record<string, string | number>

export function resolveLocale(value: string | null | undefined): Locale {
  return SUPPORTED_LOCALES.includes(value as Locale) ? (value as Locale) : DEFAULT_LOCALE
}

export function translate(locale: Locale, key: TranslationKey, variables?: Variables) {
  const template = dictionaries[locale][key] || dictionaries.en[key] || key

  if (!variables) return template

  return template.replace(/\{(\w+)\}/g, (_, name) => String(variables[name] ?? `{${name}}`))
}

